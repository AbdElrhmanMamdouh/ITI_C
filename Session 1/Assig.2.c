#include <stdio.h>

int main() {
    int num1, num2, num3;

    printf("Please Enter 1st Number: ");
    scanf("%d", &num1);

    printf("Please Enter 2nd Number: ");
    scanf("%d", &num2);

    printf("Please Enter 3rd Number: ");
    scanf("%d", &num3);

    printf("3rd Number is %d\n", num3);
    printf("2nd Number is %d\n", num2);
    printf("1st Number is %d\n", num1);

    return 0;
}
